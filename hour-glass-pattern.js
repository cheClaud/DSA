// Hour Glass Pattern

// * * * * 0 - 3(4-0-1) // 6-0-6(0) > i = i
//  * * *  1 - 2(4-1-1) // 6-1= 5(1)
//   * *   2 - 1(4-2-1) // 6-2 = 4(2)
//    *    3 - 0(4-3-1) // 6-3 = 3
//   * *   4 -  (4-4-1)-1 //6-4 = 2
//  * * *  5 -  (4-5-1)-2 //6 -5 = 1
// * * * * 6 -  (4-6-1)-3 //6-6 = 0

for (let i = 0; i < 7; i++) {
  console.log(
    " ".repeat(Math.min(6 - i, i)) + "* ".repeat(Math.abs(3 - i)) + "*"
  );
}                        
